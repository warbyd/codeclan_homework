class Event:
    def __init__(self, id, title, location, date, start_time, end_time, description, user_id,):
        self.id = id
        self.title = title
        self.location = location
        self.date = date
        self.start_time = start_time
        self.end_time = end_time
        self.description = description
        self.user_id = user_id

